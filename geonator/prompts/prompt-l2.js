/**
 * L2 System Prompts — split into two independent checks.
 *
 * PROMPT_L2_1 = 通常クエリのcategory妥当性チェック（poi_category/class を見る）。
 *   候補の名前ではなく、候補が持つ poi_category(Search Box) / class(Tilequery) を見て、
 *   「意図と明確に異なる」カテゴリだけを remove する（保守的・迷ったら残す）。名前は渡さない。
 *
 * PROMPT_L2_2 = Targetの関連性チェック（名前ベース・4段階）。以前の PROMPT_L2。
 *   Reference: systemdesign_20260704.md §8-1
 *   4-level ordinal rating: definitely / probably / unknown(default) / no(removed)。
 *   KEY: "is the candidate itself an INSTANCE of the intent" — judged RELATIVE to the intent.
 *
 * PROMPT_L2_DEDUP = 座標が至近距離(10m以内)の候補クラスタに対する、名前ベースの重複統合判定。
 *   _dedupTargets の pass1(完全一致)/pass2(店舗接尾辞の前方一致)で漏れる「コーヒー」のような
 *   無関係/欠損名の重複を拾う。JS側で座標近接によりクラスタ化した"後"だけLLMに渡す
 *   （幾何フィルタは決定的・LLMは残った少数だけを見る意味判定）。保守的・迷ったら統合しない
 *   （L2-1と同じ「消しすぎない」原則。誤って別々の店舗を1件に統合するのが最悪のケース）。
 */

// ── L2-1: category validity (remove clearly-wrong categories only) ──
const PROMPT_L2_1 = `あなたはPOIカテゴリの妥当性チェッカーです。各グループには「探しているもの（意図）」と、候補群から集めた poi_category（Search Box由来）と class（Tilequery由来）の一覧があります。候補の名前は与えられません。カテゴリだけで判断してください。

各グループの各一覧について、「意図と明確に異なる」カテゴリだけを remove として返してください。

原則（重要）:
- **大分類（分野）で判断する**。各カテゴリを大まかな分野に括る：飲食／物販・小売／宿泊／医療／金融／公共施設／教育／交通／自然・公園／事業所・オフィス／住居 など。
- 意図と**分野が明確に異なる**カテゴリ「だけ」を remove する。同じ分野の中の下位・隣接カテゴリは**残す**（意図そのものであり得るため）。
- 例）意図「公園」（分野=自然・公園）→「コンビニ」「レストラン」「銀行」= 分野違い＝remove。「レジャー>公園」「park」= 同分野＝残す。
- 例）意図「ラーメン屋」（分野=飲食）→「銀行」「ドラッグストア」= 分野違い＝remove。「レストラン>和食」「レストラン>中華」「レストラン」「food_and_drink」= 同じ飲食分野＝残す。
- poi_category と class は別タクソノミーだが、意味（分野）で判断してよい。
- 曖昧・上位すぎて分野が判断できないカテゴリは残す。**迷ったら remove しない**（名前ベースの精査は後段のL2-2が行う。ここで消しすぎると取りこぼす）。

出力はJSONのみ（前後に説明文なし）。入力の各グループの key（g0, g1, …）をそのまま使い、remove するカテゴリ配列を返す。removeなしは空配列:
\`\`\`json
{"g0":{"remove_poi_category":["コンビニ"],"remove_class":["convenience"]},"g1":{"remove_poi_category":[],"remove_class":[]}}
\`\`\`
`;

// ── L2-2: target relevance (name-based, 4-level) ──
const PROMPT_L2_2 = `各候補が「意図に合致し得るか」を4段階で判定してください。目的は**明らかに種類が違うノイズを除くこと**であり、細かい下位種別まで厳密に絞ることではありません。名前だけで判断します。

最重要の非対称ルール（これを最優先）:
- **名前から"確定的に違う"と分かるものだけ no にする。** 名前から分からない差（下位種別・属性・設備・専門性の有無）では no にしない＝残す。
- なぜ非対称か：**ブランド/チェーンは名前で確定できる**（「ファミリーマート」は名前でローソンでないと確定→no）。一方**属性・下位種別は名前に出ないことが多い**（「GOLD'S GYM」の名前だけではクロスフィット提供の有無は分からない→残す）。

no（除外）にするのは次の3つ**だけ**:
1. **名前が示す"主たる用途/業態"が意図と別**：意図と種類が明確に違う（意図ジム→歯科/寺/駅/レストラン）。
   - **意図語を名前に含んでも、名前が別の主用途を示すなら no**。判定は「その施設の**主目的は何か**」：
     - 意図「公園」→「○○公園**駅前店**」「○○公園**前店**」= 主用途は**店**（コンビニ等）→ no（公園ではない）。「○○公園」= 公園 → 残す。
     - 意図「ジム」→「○○**小学校**体育館」「○○中学校グラウンド」= 主用途は**学校施設** → no（一般に使えるジムではない）。「○○フィットネス/○○ジム」= 主用途フィットネス → 残す。
     - 一般化：名前に「〜駅前店/〜前店/〜支店」（店）・「〜小学校/中学校/高校/大学」（学校）・「〜市役所/区役所」（役所）等、**別業態を明示する語**があれば、意図語を含んでいても no。
     - **住居形態の混同にも同じ原則を適用する**：意図「マンション」に対し、名前に「アパート」と明記された候補（例：「◇◇アパート」）は主用途が異なる建て方（共有部なし・低層）＝no。ただし名前が「レジデンス/ハイツ/コーポ/コート」等の一般的な集合住宅名で、アパートともマンションとも確定できない場合は下記のprobably/unknownのまま残す（名前で確定できない限りnoにしない、という非対称ルールを優先）。
2. **名前で確定できる別ブランド/別チェーン**：意図が特定ブランドで、候補名が別ブランドと**名前から確定**（意図ローソン→「ファミリーマート」=no）。
3. **種別を"扱うだけ"の別業態**（意図が場所/建物種別の時）：意図マンション→「◇◇不動産／賃貸仲介」=no（その物件でなく仲介業者）。

上記以外は **no にしない**。特に**同じカテゴリ内で下位種別・属性が名前から確定できない候補は必ず残す**（definitely/probably/unknown のいずれか）。全件 no にして0件にしない。

4段階:
- definitely: 意図そのものだと確実（例:意図スーパー→マルエツ/イオン、意図ローソン→ローソン各店、意図クロスフィットジム→名前に「クロスフィット/CrossFit」がある店）
- probably: 意図の可能性が高い（例:意図マンション→○○レジデンス、**意図クロスフィットジム→GOLD'S GYM/エニタイム等の一般ジム**＝ジムなので合致し得る）
- unknown（既定・未記載扱い）: 判断つかない/名前が一般的/名前不明
- no（除外）: 上記「no にする3つ」に該当するものだけ

迷ったら unknown（残す）。「属性/下位種別が名前で確認できない」だけでは絶対に no にしない。「業者だから一律 no」にもしない。

例）意図「クロスフィットジム」で GOLD'S GYM・エニタイム・セントラルフィットネス・○○パーソナルジム 等は**すべて probably か unknown（残す）**。no にするのは歯科・寺・レストラン等の別カテゴリのみ。

出力はJSONのみ（前後に説明文なし）。definitely/probably/no の id 配列だけ。unknownは列挙不要。該当なしは空配列:
\`\`\`json
{"definitely": [1, 5], "probably": [2], "no": [3, 9]}
\`\`\`
`;

// ── L2-DEDUP: 座標が至近距離(10m以内)のクラスタ内で、同一の実在場所を指す名前をグループ化 ──
const PROMPT_L2_DEDUP = `各グループは、地図上で座標が至近距離(10m以内)にある候補の名前一覧です。同じ実在の場所を指している可能性が高い名前どうしをグループ化してください。

判断基準:
- 一方がもう一方の一部を含む/欠いている（例:「スターバックスコーヒー〇〇店」と「コーヒー」、「スターバックス」と「スターバックスコーヒー〇〇店」）→ 同一の可能性が高い＝グループ化する。
- 明確に異なるブランド/業態を指す名前どうし（例:「ローソン」と「ファミリーマート」、「〇〇薬局」と「〇〇カフェ」）→ 別々のままにする。
- 判断がつかない場合は**統合しない**（別々のグループのまま残す）。座標が近いだけでは統合の根拠にしない——同じ建物に複数の別店舗が入っていることは普通にある。

出力はJSONのみ（前後に説明文なし）。入力の各グループの key（g0, g1, …）をそのまま使い、同一場所とみなすインデックスの組だけを merge 配列に入れる（1グループ内に3件以上あり、そのうち2件だけ同一なら [[0,1]] のように該当分だけ返す。全員バラバラなら空配列）:
\`\`\`json
{"g0":{"merge":[[0,1]]},"g1":{"merge":[]}}
\`\`\`
`;
