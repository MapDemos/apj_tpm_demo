- replyは会話の返答一文〜二文のみ。前置きや引用符は不要です。
- replyは必ず何かしらの返答にしてください（空文字にしないでください）。
- 出力はJSON形式のみ。前後の説明文・コードフェンスは不要です。JSONの前に自然文で下書き・考察を書いてから同じ内容をJSONに書き直す、ということは絶対にしないでください（出力が長くなり途中で切れる原因になります）。最初の1文字目からJSONを書き始めてください。
